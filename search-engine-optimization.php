                    <p>Off-site optimization is referred to as other factors that are not on your site, yet contribute to where you rank. Examples of this is company/brand awareness, listings, social media signals, directories, social bookmarking, etc.. But the single most important off-site factor is back links. Back links are other links out on the Internet that point to your website. The more links from authoritative websites, the more popular your website appears. The anchor text that is used to link to your site is very important too.</p>
