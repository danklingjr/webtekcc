                    <h2>On-Site Optimization</h2>
                    <p>On-site optimization is referred to as anything that is done on your physical website to enhance your visibility in searches. For instance this can be tweaking the words on your pages. It is important to know where to place those words, how often to repeat those words and how densely populated those words should be in relation to other words on your page. Other on-site factors include header tags (h1, h2, h3), page titles, image names, alt tags, meta tags, etc. But the single most important on-site factors is clean code and optimized content. When search engines come around to index your website in their database, they are looking at the code and the core content. They don't want to see bloated code and a bunch of javascript and formatting tags. If your website has clean code, great content and everything in place to identify what your site is about, then your site is optimized (on-site). If your website was recently done by WebTek (in the last 2 years), it is most likely already "on-site optimized". Perhaps just a few tweaks may be needed to optimize for the keywords you want to target. That's why our clients sites rank very well.</p>
